package DataTypes;

public class STRINGCONVERSION {
	public static void main(String[] args) {
		
	
	String s1="996632256";
	String s2="5678";
	System.out.println("without conversion:"+(s1+s2));

	int a=Integer.parseInt(s1);
	int b=Integer.parseInt(s2);
	System.out.println("After conversion:"+(a+b));
	
	
	
	}

}
