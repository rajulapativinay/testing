package Operators;

public class ComparisionOperator {

	public static void main(String[] args) {
		// Relational r comparision operator--> < > <= >= == !=
		int a=10;
		int b=20;
		System.out.println(a<b);//true
		System.out.println(a>b);//false
		System.out.println(a<=b);//true
		System.out.println(a>=b);//false
		System.out.println(a==b);//false
		System.out.println(a!=b);//true
		
	}

}
