package Operators;

public class ArthamaticOperator {

	public static void main(String[] args) {
// operator is a symbol to perform operations between the two or more operands
	//Arthimatic Operator--> + - * % /
		int a=100;
		int b=20;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);

	}

}
