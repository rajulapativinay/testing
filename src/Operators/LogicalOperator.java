package Operators;

public class LogicalOperator {

	public static void main(String[] args) {
		//logical operator-->&& || !
		boolean a=true;
		boolean b=false;
		System.out.println(a&&b);//false
		System.out.println(a||b);//true
		System.out.println(!a);//false
		System.out.println(!b);//true
		System.out.println(!a&&b);//false
		System.out.println(!a||b);//false
		
	}
}
