package Operators;

public class IncrementDecrement {

	public static void main(String[] args) {
 //IncrementDecrement Operator-->++ --
		int a=50;
		a=a+1;//a++
		System.out.println(a);
		
	

		int b=100;
		b=b-1;//b--
		System.out.println(b);
		
		
		int c=10;
		c=c--;
		System.out.println(c);
		
		
		int d=20;
		d=++d;
		System.out.println(d);
		
	}
}


